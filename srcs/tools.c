int		is_valid_mapchar(char c)
{
	if (c == '-' || c == ',' || (c >= '0' && c <= '9') || (c >= 'A' && c <= 'F')
			|| (c >= 'a' && c<= 'f') || c == 'x' || c == 'X' || c == '\n'
			|| c == ' ' || c == '\t')
		return (1);
	return (0);
}

t_ipos	get_ipos(int i, int j)
{
	t_pos	res;

	res.i = i;
	res.j = j;
	return (res);
}
